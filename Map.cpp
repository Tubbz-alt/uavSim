Map::Map()
{
    // background = TextureManager::loadTexture("img/back.bmp");
    blueBack = TextureManager::loadTexture("img/blue.png");
    whiteback = TextureManager::loadTexture("img/white.png");
    // grass = TextureManager::loadTexture("img/grass.png");
    // water = TextureManager::loadTexture("img/water.png");
    Map::loadMap(level1);
    srcD.x = srcD.y = 0;
    srcD.w = dstD.w = 64;
    srcD.h = dstD.h = 32;
    dstD.x = dstD.y = 0;
}

void Map::loadMap(int arr[20][25])
{
    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::drawMap()
{
    int typeBack = 0; //can have multi things there

    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            typeBack = map[row][column];
            dstD.x = row * 64;
            dstD.y = column * 32;

            switch (typeBack)
            {
            case 0:
                TextureManager::draw(whiteback, srcD, dstD);
                break;
            case 1:
                TextureManager::draw(blueBack, srcD, dstD);
                break;

            default:
                break;
            }
        }
    }
}